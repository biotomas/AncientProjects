package cz.matfyz.ctrlaltdel.modeler.generators;

import cz.matfyz.ctrlaltdel.modeler.ModellingException;

/**
 * Invalid range settings.
 */
public class RangeSettingsException extends ModellingException {

	/**
	 * Serialization ID.
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * @param message			Message to be shown.
	 */
	public RangeSettingsException(String message) {
		super(message);
	}

	/**
	 * @param innnerException	Inner exception causing this error.
	 */
	public RangeSettingsException(Throwable innnerException) {
		super(innnerException);
	}

	/**
	 * @param message			Message to be shown.
	 * @param innnerException	Inner exception causing this error.
	 */
	public RangeSettingsException(String message, Throwable innnerException) {
		super(message, innnerException);
	}

}
